package com.ty.springs.prac;

public class Teacher {
	
	private String name;
	private String school;
	
	
	public Teacher(String name, String school) {
		super();
		this.name = name;
		this.school = school;
	}


	public String getName() {
		return name;
	}


	public String getSchool() {
		return school;
	}
	
	
	

}
